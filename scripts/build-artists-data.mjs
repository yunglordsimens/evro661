#!/usr/bin/env node
// Scans artists-data/{slug}/ and generates src/data/artists.js
// Source of truth for names/ids/blocks: ../artists.js
// Run: node scripts/build-artists-data.mjs

import { readdir, readFile, mkdir, writeFile } from 'fs/promises';
import { existsSync } from 'fs';
import { join, extname, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const DATA_DIR = join(ROOT, 'artists-data');
const OUT_FILE = join(ROOT, 'src', 'data', 'artists.js');

const { artists: SOURCE } = await import('../artists.js');

const IMG_EXTS = new Set(['.jpg', '.jpeg', '.png', '.gif', '.webp']);
const VID_EXTS = new Set(['.mp4', '.mov', '.webm']);
const IMG_RE = /^photo-\d+\./i;
const VID_RE = /^video(-\d+)?\./i;

async function scanSlug(slug) {
  const dir = join(DATA_DIR, slug);
  const imgs = [];
  const vids = [];
  let bio = null;

  try {
    const files = (await readdir(dir)).sort();
    for (const file of files) {
      const ext = extname(file).toLowerCase();
      if (IMG_RE.test(file) && IMG_EXTS.has(ext)) {
        imgs.push(`artists-data/${slug}/${file}`);
      } else if (VID_RE.test(file) && VID_EXTS.has(ext)) {
        vids.push(`artists-data/${slug}/${file}`);
      }
    }
  } catch {
    // folder missing — all null
  }

  const bioPath = join(DATA_DIR, slug, 'bio.md');
  if (existsSync(bioPath)) {
    bio = (await readFile(bioPath, 'utf8')).trim();
  }

  return {
    imgs: imgs.length ? imgs : null,
    vids: vids.length ? vids.slice(0, 3) : null,
    bio,
  };
}

// ── Build ────────────────────────────────────────────────────────────────────

const built = [];
for (const src of SOURCE) {
  const assets = await scanSlug(src.slug);
  built.push({ id: src.id, block: src.block, slug: src.slug, name: src.name, ...assets });
}

// ── Render JS ────────────────────────────────────────────────────────────────

function renderArtist(a) {
  const lines = [];
  const warn = [];
  if (!a.bio)  warn.push('no bio');
  if (!a.imgs) warn.push('no photo');
  if (!a.vids) warn.push('no video');
  const tag = warn.length ? `  // ⚠️ ${warn.join(', ')}` : '';

  lines.push(`  {${tag}`);
  lines.push(`    id: ${a.id}, block: ${a.block}, slug: ${JSON.stringify(a.slug)},`);
  lines.push(`    name: ${JSON.stringify(a.name)},`);
  lines.push(`    bio: ${a.bio === null ? 'null' : JSON.stringify(a.bio)},`);
  lines.push(`    imgs: ${JSON.stringify(a.imgs)},`);
  lines.push(`    vids: ${JSON.stringify(a.vids)},`);
  lines.push(`  },`);
  return lines.join('\n');
}

const blocks = { 1: [], 2: [], 3: [] };
for (const a of built) blocks[a.block].push(a);

const sections = [1, 2, 3].map(b =>
  `  // ── Block ${b} ${'─'.repeat(73)}\n\n` +
  blocks[b].map(renderArtist).join('\n\n')
).join('\n\n');

const output = `// AUTO-GENERATED by scripts/build-artists-data.mjs — do not edit by hand
// Generated: ${new Date().toISOString()}
// Source of truth for names/ids/blocks: artists.js (root)
// Assets scanned from: artists-data/{slug}/

export const artists = [

${sections}

];
`;

await mkdir(join(ROOT, 'src', 'data'), { recursive: true });
await writeFile(OUT_FILE, output, 'utf8');

// ── Report ───────────────────────────────────────────────────────────────────

console.log(`✅  Written ${built.length} artists → src/data/artists.js\n`);

const missing = {
  bio:   built.filter(a => !a.bio).map(a => a.slug),
  imgs:  built.filter(a => !a.imgs).map(a => a.slug),
  vids:  built.filter(a => !a.vids).map(a => a.slug),
};

if (missing.bio.length)  console.log(`⚠️  No bio:   ${missing.bio.join(', ')}`);
if (missing.imgs.length) console.log(`⚠️  No photo: ${missing.imgs.join(', ')}`);
if (missing.vids.length) console.log(`⚠️  No video: ${missing.vids.join(', ')}`);

// Fully missing (all three null)
const empty = built.filter(a => !a.bio && !a.imgs && !a.vids);
if (empty.length) {
  console.log(`\n🚨  ENTIRELY MISSING (contact artist): ${empty.map(a => a.slug).join(', ')}`);
}
